# Lista heterogénea(distintos tipos de datos)
lista=["Lunes","Martes","Miércoles","Jueves",
       "Viernes","Sábado","Domingo", True]

# Imprimir el contenido de la lista 'Lista'
print(lista)

# Imprimir el contenido de 'Lista[1]=Martes'
print(lista[1])

# Imprimir el contenido de 'Lista[-1]=True'
print(lista[-1])    

# Longitud de 'Lista' con método 'len()'
print(len(lista))

# Lista2
nombres=["Juan","Héctor",1,2,3,False,7.1]
print(nombres[-2])
print(nombres[5])
print(len(nombres))

# Modificando la lista 'nombres[1]=Héctor'
nombres[1]=3
print(nombres)









